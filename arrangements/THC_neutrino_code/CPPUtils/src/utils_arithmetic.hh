#ifndef UTILS_ARITHMETIC_HH
#define UTILS_ARITHMETIC_HH

#include <cmath>
#include <limits>

namespace utils {

//! Returns the sign of a
template<typename T>
inline T sign(T a) {
    return a > 0 ? T(1) : T(-1);
}

} // namespace

#endif
